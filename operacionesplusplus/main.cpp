// Practica para refrescar la sintaxis de C++

#include <iostream>
using namespace std;

int main()
{
    // ---------------------------------------------

    /* OPERADORES MATEMATICOS
        Suma -> +
        Resta -> -
        Multiplicacion -> *
        Division -> /
        Modulo -> %
    */

    int numero1 = 7;
    int numero2 = 3;
    int cociente = numero1/numero2;
    int mod = numero1%numero2;

    cout<<cociente<<endl;
    cout<<mod<<endl;

    return 0;
}
